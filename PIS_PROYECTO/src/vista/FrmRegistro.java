    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        fila = tblRegistro.getSelectedRow();
        if (fila >= 0) {
            try {
                Integer id = md.getLista().obtener(fila).getId();
                pd.setPersona(pd.obtener(id));
                txtNombre.setText(pd.getPersona().getNombres_completos());
                txtIdentificacion.setText(Integer.toString(pd.getPersona().getIdentificacion()));
                txtCorreoEl.setText(pd.getPersona().getCorreo());
                lblGenero.setText(pd.getPersona().getGenero().toString());
            } catch (Exception e) {
            }

        } else {
            JOptionPane.showMessageDialog(null, "Escoja un registro de la tabla", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

