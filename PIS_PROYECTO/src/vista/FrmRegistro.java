    private void cargarTabla(){
        md.setLista(pd.listar());
        tblRegistro.setModel(md);
        tblRegistro.updateUI();
    }
    
    private void guardarPersona() {
        try {
            if (txtNombre.getText().isEmpty() || !txtCorreoEl.getText().isEmpty()
                    || !txtIdentificacion.getText().isEmpty()) {
                this.pd.getPersona().setNombres_completos(txtNombre.getText());
                this.pd.getPersona().setIdentificacion(Integer.parseInt(txtIdentificacion.getText()));
                this.pd.getPersona().setCorreo(txtCorreoEl.getText());
                if (pd.getPersona().getId() != null) {
                    pd.modificar(fila);
                } else {
                    pd.guardar();
                }
               cargarTabla();

                JOptionPane.showMessageDialog(null, "Se ha guardado ", "INFORMACION", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Llene todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            System.out.println(e);
        }

    }

